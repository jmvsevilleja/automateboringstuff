# Orders of operations
# 1 ** exponent
# 2 * Multiplication
# 3 / Division
# 4 // Interger Division
# 5 % Modulus
# 6 - Substruction
# 7 + Addition

result = 8 ** 7 * 8 / 5 // 4 % 50 - 2 + 1
print(result)
result = 8 ** 7
print(result)
result = result * 8
print(result)
result = result / 5
print(result)
result = result // 4
print(result)
result = result % 50
print(result)
result = result - 2
print(result)
result = result + 1
print(result)

# String Replication
